        public static (int eastings, int northings) GetGridPoint(string crs)
        {
            int eastings = int.MaxValue;
            int northings = int.MaxValue;
            if (CrsToGrid.TryGetValue(crs, out var en))
            {
                eastings = en.eastings;
                northings = en.northings;
            }
            return (eastings, northings);
        }

        public static UInt32 GetInterStationDistance(string crs1, string crs2)
        {
            var (eastings1, northings1) = GetGridPoint(crs1);
            var (eastings2, northings2) = GetGridPoint(crs2);
            var xdiff = eastings1 - eastings2;
            var ydiff = northings1 - northings2;
            var crowFlies = Math.Sqrt((1.0 * xdiff * xdiff + 1.0 * ydiff * ydiff));
            return (UInt32)crowFlies;
        }

        public static IEnumerable<(string crs, int eastings, int northings)> AllGeoCrs => CrsToGrid.Select(x => (x.Key, x.Value.Item1, x.Value.Item2));
    }

